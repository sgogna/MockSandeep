package spring;

public class Configuration {
private String recDir;

private String customTagsFilePath;


public String getRecDir() {
	return recDir;
}

public void setRecDir(String recDir) {
	this.recDir = recDir;
}

public String getCustomTagsFilePath() {
	return customTagsFilePath;
}

public void setCustomTagsFilePath(String customTafsFilePath) {
	this.customTagsFilePath = customTafsFilePath;
}

}
